# Convert temperature
celsius_temperature = float(input("Enter temperature in Celsius: "))
fahrenheit_temperature = (celsius_temperature * 9/5) + 32

print(f"{celsius_temperature} Celsius is equal to {fahrenheit_temperature} Fahrenheit")